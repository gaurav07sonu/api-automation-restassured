package com.sentieo.constants;

public class Configuration {
	private String envName;
	private String appURL;
	private String userName;
	private String password;
	private String userAppUrl;
	private String publicApiUrl;
	private String xapikey;
	private String xuserkey;

	public String getAppURL() {
		return appURL;
	}

	public void setAppURL(String appURL) {
		this.appURL = appURL;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getEnvName() {
		return envName;
	}

	public void setEnvName(String envName) {
		this.envName = envName;
	}

	public String getUserAppUrl() {
		return userAppUrl;
	}

	public void setUserAppUrl(String userAppUrl) {
		this.userAppUrl = userAppUrl;
	}

	public String getPublicApiUrl() {
		return publicApiUrl;
	}

	public void setPublicApiUrl(String publicApiUrl) {
		this.publicApiUrl = publicApiUrl;
	}

	public String getXapikey() {
		return xapikey;
	}

	public void setXapikey(String xapikey) {
		this.xapikey = xapikey;
	}

	public String getXuserkey() {
		return xuserkey;
	}

	public void setXuserkey(String xuserkey) {
		this.xuserkey = xuserkey;
	}

}
